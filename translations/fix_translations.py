for lang, trans in replacements.items():
    fname = f'translation_{lang}.xml'
    try:
        with open(fname, 'r', encoding='utf-8') as f:
            content = f.read()
    except FileNotFoundError:
        print(f'{lang}: FILE NOT FOUND')
        continue

    replaced = 0
    for key, value in trans.items():
        pattern = re.compile(r'(<e k="' + re.escape(key) + r'" v=")(\[EN\] [^"]*)(")')
        new_content = pattern.sub(lambda m: m.group(1) + value + m.group(3), content)
        if new_content != content:
            replaced += 1
        content = new_content

    with open(fname, 'w', encoding='utf-8') as f:
        f.write(content)

    remaining = content.count('[EN] ')
    print(f'{lang}: replaced {replaced}, {remaining} [EN] entries remaining')

print('Done.')
